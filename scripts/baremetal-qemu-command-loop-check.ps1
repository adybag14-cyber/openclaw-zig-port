    Get-Content -Path $gdbStdout -Tail 80
}
if (Test-Path $gdbStderr) {
    Get-Content -Path $gdbStderr -Tail 80
}
if (Test-Path $qemuStderr) {
    Get-Content -Path $qemuStderr -Tail 80
}
exit 1
