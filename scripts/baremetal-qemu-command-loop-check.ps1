Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_ACK=$ack"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_LAST_OPCODE=$lastOpcode"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_LAST_RESULT=$lastResult"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_TICKS=$ticks"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_TICK_BATCH_HINT=$tickBatchHint"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_MAILBOX_OPCODE=$mailboxOpcode"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_MAILBOX_SEQ=$mailboxSeq"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_GDB_STDOUT=$gdbStdout"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_GDB_STDERR=$gdbStderr"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_QEMU_STDOUT=$qemuStdout"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_QEMU_STDERR=$qemuStderr"
Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_TIMED_OUT=$timedOut"

$pass = (
    $hitStart -and
    $hitAfterCommand -and
    (-not $timedOut) -and
    $ack -eq 1 -and
    $lastOpcode -eq $commandOpcode -and
    $lastResult -eq 0 -and
    $ticks -eq $commandArg0 -and
    $tickBatchHint -eq $commandArg0 -and
    $mailboxOpcode -eq $commandOpcode -and
    $mailboxSeq -eq 1
)

if ($pass) {
    Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_PROBE=pass"
    exit 0
}

Write-Output "BAREMETAL_QEMU_COMMAND_LOOP_PROBE=fail"
